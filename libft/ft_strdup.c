#include "libft.h"

char	*ft_strdup(const char *s1)
{
	unsigned int	s1len;
	char			*new;
	int				i;

	s1len = ft_strlen(s1);
	new = malloc(sizeof(char) * (s1len + 1));
	if (new == NULL)
		return (0);
	i = 0;
	while (s1[i])
	{
		new[i] = s1[i];
		i++;
	}
	new[i] = '\0';
	return (new);
}
